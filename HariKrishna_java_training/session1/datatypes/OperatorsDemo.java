package session1.datatypes;

public class OperatorsDemo {

    public static void main(String[] args) {

        int a = 10, b = 5;

        // Arithmetic
        System.out.println("Addition: " + (a + b));

        // Relational
        System.out.println("a > b: " + (a > b));

        // Logical
        System.out.println("(a > 5 && b < 10): " + (a > 5 && b < 10));
    }
}